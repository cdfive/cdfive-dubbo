public class IntegerTest4 {

    public static void main(String[] args) {
        System.out.println(1 << 3); // 8
        System.out.println(1 << 2); // 4
        System.out.println(1 << 1); // 2
        System.out.println(1 << 0); // 1
        System.out.println(1 << -1); // -2147483648
    }
}
