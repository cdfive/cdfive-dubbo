/**
 * @author cdfive
 */
public class CharIntegerDoubleAddOperationTest {

    public static void main(String[] args) {
        // 98
        System.out.println('a' + 1);

        // 98.618
        System.out.println('a' + 1 + 0.618);

        // a1
        System.out.println("a" + 1);

        //a10.618
        System.out.println("a" + 1 + 0.618);
    }
}
